public class JavaArithOrder {
  public static void main(String[] args) {

    /* Java Order Of Operations
    *  Works just like PEMDAS
    */
    System.out.println(10 * 2 + 15 / 5 - 4);

    /* Java Grouping
    * Use () if you're worried
    */
    System.out.println((10 * 2) + (15 / 5) - 4);
  }
}
