public class Printing {
  public static void main(String[] args) {

    System.out.print("Hello");
    System.out.print(",");
    System.out.print("World");
    System.out.println();
    System.out.println("3LIT3 H4X0R!");

    System.out.println(""); // But what if I want to have a quote print??

    System.out.println("\""); // Introducing escape sequences!!

    System.out.println("Hi \t There \t Dude");

    System.out.println();

    System.out.println("Hi \nmy \nname \nis \nwhat \nmy \nname \nis \nwicka wicka \nslim shadyyyyy");
  }
}
